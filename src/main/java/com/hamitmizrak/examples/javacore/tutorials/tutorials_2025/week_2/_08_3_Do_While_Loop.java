package com.hamitmizrak.examples.javacore.tutorials.tutorials_2025.week_2;

public class _08_3_Do_While_Loop {
    public static void main(String[] args) {
        // Do-While Loop
        // do while : her şartta bir kere çalışsın

        int i = 1;
        do {
            System.out.print(i + " ");
            i++;
        } while (i <= 10);
    }
}